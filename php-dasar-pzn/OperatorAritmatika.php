<?php

// Negatif
var_dump(-5);

// Positif / tidak perlu ditambahkan +
var_dump(+5);

// Tambah
var_dump(1 + 1);

// Kurang
var_dump(1 - 1);

// Kali
var_dump(4 * 2);

// Bagi
var_dump(4 / 2);

// Sisa Bagi
var_dump(4 % 2);

// Pangkat
var_dump(2 ** 3);


// Operator Penugasan Aritmatika
$a = 10;

// Tambah
var_dump($a += 10);

// Kurang
var_dump($a -= 10);

// Kali
var_dump($a *= 10);

// Bagi
var_dump($a /= 10);

// Sisa bagi
var_dump($a %= 10);
