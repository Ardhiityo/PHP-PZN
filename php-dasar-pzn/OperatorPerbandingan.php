<?php

// Sama dengan
var_dump(1 == "1"); // true

// Sama dengan tipe data yang sama
var_dump(1 === "1"); // false

// Tidak sama dengan
var_dump(1 != 1); // false

// Tidak sama dengan tipe data yang sama
var_dump(1 !== '1'); // true

// Tidak sama dengan
var_dump(1 <> 1); // false

// Lebih dari
var_dump(1 > 2); // false

// Lebih dari sama dengan
var_dump(1 >= 2); // false

// Kurang dari
var_dump(1 < 2); // true

// Kurang dari sama dwngan
var_dump(1 <= 2); // true
